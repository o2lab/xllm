def format_subject(subject):
    l = subject.split("_")
    s = ""
    for entry in l:
        s += " " + entry
    return s


def format_example(df, idx, include_answer=True):
    prompt = df.iloc[idx, 0]
    k = df.shape[1] - 2
    for j in range(k):
        prompt += "\n{}. {}".format(choices[j], df.iloc[idx, j + 1])
    prompt += "\nAnswer:"
    if include_answer:
        prompt += " {}\n\n".format(df.iloc[idx, k + 1])
    return prompt


def gen_prompt(train_df, subject, k=-1):
    prompt = "The following are multiple choice questions (with answers) about {}.\n\n".format(
        format_subject(subject)
    )
    if k == -1:
        k = train_df.shape[0]
    for i in range(k):
        prompt += format_example(train_df, i)
    return prompt


@torch.no_grad()
def eval(args, subject, model, encode, decode, dev_df, test_df):
    cors = []
    all_probs = []
    answers = choices[: test_df.shape[1] - 2]

    for i in range(test_df.shape[0]):
        # get prompt and make sure it fits
        k = args.ntrain
        prompt_end = format_example(test_df, i, include_answer=False)
        train_prompt = gen_prompt(dev_df, subject, k)
        prompt = train_prompt + prompt_end
        # print(prompt)
        start_ids  = encode(prompt)
        input_ids = (torch.tensor(start_ids, dtype=torch.long, device=device)[None, ...])


        while input_ids.shape[-1] > 2048:
            k -= 1
            train_prompt = gen_prompt(dev_df, subject, k)
            prompt = train_prompt + prompt_end
            # print(prompt)
            start_ids  = encode(prompt)
            input_ids = (torch.tensor(start_ids, dtype=torch.long, device=device)[None, ...])

        label = test_df.iloc[i, test_df.shape[1] - 1]

        # print(prompt)
        # print(label)
        # exit(0)
        # print("Lable = ", label)
        # start_ids = encode("")
        # decoder_input_ids = (torch.tensor(start_ids, dtype=torch.long, device=device)[None, ...])
        # decoder_input_ids = model._shift_right(decoder_input_ids)
        # logits = model(
        #     input_ids=input_ids, #decoder_input_ids=decoder_input_ids
        # ).logits.flatten()
        # print(input_ids)
        with torch.no_grad():
            with ctx:
                # print("Begin")
                logits= model.generate(input_ids, 1.0)[0].tolist()
                # print("END")
        # print(logits)
        probs = (
            torch.nn.functional.softmax(
                torch.tensor(
                    [
                        logits[encode("A")[0]],
                        logits[encode("B")[0]],
                        logits[encode("C")[0]],
                        logits[encode("D")[0]],
                    ]
                ).double(),
                dim=0,
            )
            .detach()
            .cpu()
            .numpy()
        )
        pred = {0: "A", 1: "B", 2: "C", 3: "D"}[np.argmax(probs)]

        cor = pred == label
        cors.append(cor)
        all_probs.append(probs)

    acc = np.mean(cors)
    cors = np.array(cors)

    all_probs = np.array(all_probs)
    print("Average accuracy {:.3f} - {}".format(acc, subject))

    return cors, acc, all_probs


